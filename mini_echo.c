#include "minishell.h"

void	mini_echo(char **cmd)
{
	int	i;

	i = 1;
	while (cmd[i])
	{
		write(1, " ", i != 1);
		ft_putstr(cmd[i++]);
	}
	write(1, "\n", 1);
}
